//função de gerar assento

function assentoE(min, max){
    min = Math.ceil(15);
    max = Math.floor(62);
    return Math.floor(Math.random() * (max - min)) + min;
}
function assentoV(min, max){
    min = Math.ceil(1);
    max = Math.floor(14);
    return Math.floor(Math.random() * (max - min)) + min;
}




